public class customIdModel {

    private String first;
    private int second;


    public customIdModel() {
    }

    public customIdModel(String first, int second) {
        this.first = first;
        this.second = second;
    }

    public String getFirst() {
        return first;
    }

    public int getSecond() {
        return second;
    }
}
